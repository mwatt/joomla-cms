/**
* @package Joomla
* @subpackage Config
*/
class TOOLBAR_config {
	function _DEFAULT() {
		global $_LANG;
		
		mosMenuBar::startTable();
		mosMenuBar::title( $_LANG->_( 'Global Configuration' ), 'config.png', 'index2.php?option=com_config&amp;hidemainmenu=1' );
		mosMenuBar::save();
		mosMenuBar::spacer();
		mosMenuBar::apply();
		mosMenuBar::spacer();
		mosMenuBar::cancel();
		mosMenuBar::spacer();
		mosMenuBar::help( 'screen.config' );
		mosMenuBar::endTable();
	}
}
